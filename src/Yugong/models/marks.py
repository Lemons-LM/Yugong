class Marks:
    """
    Marks
    -----
    Marks are used for quickly use the common marks of the syntax
    """
    lbrace: str = '{'
    rbrace: str = '}'
    lbracket: str = '['
    rbracket: str = ']'
    langle: str = '<'
    rangle: str = '>'
    lparen: str = '('
    rparen: str = ')'
    pipe: str = '|'
